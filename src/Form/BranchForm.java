    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int index = tableCN.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 chi nhánh trong bảng để xóa", "Thông Báo", 1);
            return;
        }

        int tk = JOptionPane.showConfirmDialog(this,"Bạn có chắc muốn xóa chi nhánh này?","Xác nhận",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (tk==JOptionPane.YES_OPTION)
        {
            if (lpd.Xoachinhanh(tableCN.getValueAt(index,0).toString()))
            {
                JOptionPane.showMessageDialog(this, "Xoá chi nhánh thành công!", "Xóa chi nhánh", 1);
                loadTable();
                autoID();
                reset();
            } else {
                JOptionPane.showMessageDialog(this, "Xoá chi nhánh thất bại!", "Xóa chi nhánh", 0);
            }

        }
        else
        {
            return;

        }

    }//GEN-LAST:event_jButton4ActionPerformed

