    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtID.getText().equals("ID")) {
            JOptionPane.showMessageDialog(this,"Hãy điền mã chi nhánh");
        } else if(checkMa(txtID.getText())==true){
            JOptionPane.showMessageDialog(this,"Mã chi nhánh bị trùng");
        } else if(txtVT.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Hãy điền vị trí");
        }else if(txtTen.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Hãy điền tên chi nhánh");
        }else {
            Branch sp =  new Branch();

            sp.setBranch_id(txtID.getText());
            sp.setLocation(txtVT.getText());
            sp.setAddress(txtTen.getText());

            if (lpd.luuchinhanh(sp)>0 )
            {
                JOptionPane.showMessageDialog(null, "Thêm chi nhánh thành công");
                loadTable();
                autoID();
                reset();
            }
            else
            JOptionPane.showMessageDialog(null, "Thêm thất bại ( Trùng chi nhánh ) !!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

